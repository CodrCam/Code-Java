package Code30;

// The Entry class is a helper class for the Hashtable. It represents a key-value pair in the hashtable.
public class Entry {
    // Fields to hold the key and value for an entry.
    String key;
    String value;

    // Constructor to create a new entry with a given key and value.
    Entry(String key, String value) {
        this.key = key;
        this.value = value;
    }
}

